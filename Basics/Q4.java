// Write a Java program that demonstrates the use of increment (++), decrement (--), and modulus (%)
// operators. Print the results.

public class Q4 {
    public static void main(String[] args) {
     int x = 10, y = 11;
     System.out.println("Original Value: "+(x));
     System.out.println("After Increment: "+(++x)); 
     System.out.println("After Increment: "+(--x)); 
     System.out.println("Remainder: "+(y%x));  

    }
}
// Original Value: 10
// After Increment: 11
// After Decrement: 10
// Remainder: 1